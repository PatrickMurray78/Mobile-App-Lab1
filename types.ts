//boolean
let flag: boolean = true;
console.log("Value assigned to flag is: " +  flag);

//number
let num: number = 67.8;
console.log("Value assigned to num is: " +  num);

//string
let str: string = "Hello";
console.log("Value assigned to str is: " +  str);

//array
let myArray: Array<number> = [1, 2, 3, 4, 5];
console.log("Value assigned to myArray is: " +  myArray);

//any
let anything: any = "This is anything";
console.log("Value assigned to anything is: " +  anything);
